// Multiples of 3 and 5
//
// Problem 1
// If we list all the natural numbers below 10 that are multiples of 3 or 5,
// we get 3, 5, 6 and 9. The sum of these multiples is 23.
//
// Find the sum of all the multiples of 3 or 5 below 1000.

// Pseudo-code strategy: Loop through the range of numbers, if a number is a multiple of three or five,
// set x equal to x + num and continue

function multipleSum(limitingNumber) {

    var sum = 0;

    for (var i = 1; i < limitingNumber; i++) {

        if (i % 3 === 0 || i % 5 === 0) {

            console.log(i);
            sum += i;
        }
    }

    return sum;
}

